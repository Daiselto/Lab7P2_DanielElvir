    private void AgregarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarVehiculoActionPerformed
        try {
            String Marca = tf_Marca.getText();
            String Modelo = tf_Modelo.getText();
            Color color = ColorBttn.getBackground();
            int A単o = (int) tf_A単o.getValue();
            double Precio_Venta = Double.parseDouble(PrecioVenta.getText());

            vehiculos.add(new Vehiculo(Marca, color, Modelo, A単o, Precio_Venta));

            FileWriter fw = null;
            BufferedWriter bw = null;
            try {
                File archivo = new File("./Vehiculo.txt");
                fw = new FileWriter(archivo, false);
                bw = new BufferedWriter(fw);
                for (Vehiculo t : vehiculos) {
                    bw.write("[\n"
                            + "\t"+t.getMarca()+", \n"
                            + "\t"+t.getModelo()+", \n"                            
                            + "\t"+t.getA単o()+", \n"
                            + "\t"+t.getPrecio_Venta()+", \n"
                            + "]-\n");
                }
                bw.flush();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            bw.close();
            fw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurri坦 un error y no se agreg坦");
        }
        JOptionPane.showMessageDialog(this, "Se agreg坦 exitosamente");
        tf_Marca.setText("");
        tf_Modelo.setText("");
        tf_A単o.setValue(2000);
        PrecioVenta.setText("");
        ColorBttn.setBackground(Color.WHITE);
    }//GEN-LAST:event_AgregarVehiculoActionPerformed

